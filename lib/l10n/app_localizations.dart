extension AppLocalizationsExtension on BuildContext {
  String tr(String key) {
    try {
      final locale = read<LocaleProvider>().locale;
      return AppLocalizations.t(locale, key);
    } catch (_) {
      return AppLocalizations.t(const Locale('ru'), key);
    }
  }
}
